const { sum, subtraction, multiply, divide } = require("../app/function/function")
describe("функция sum", () => {
  test("Сложение простых чисел", () => {
    expect(sum(1, 2)).toBe(3)
    expect(sum(10, 12)).toBe(22)
    expect(sum(99, 1)).toBe(100)
    expect(sum(1, 1)).toBe(2)
    expect(sum(1, 0)).toBe(1)
    expect(sum(0, 0)).toBe(0)
    expect(sum(+2, +5)).toBe(7)
    expect(sum(4, +1)).toBe(5)
  })
  test("Сложение float значений", () => {
    expect(sum(0.1, 0.2)).toBeCloseTo(0.3)
    expect(sum(+0.1, 2.2)).toBeCloseTo(2.3)
    expect(sum(0.1, 0.29)).toBeCloseTo(0.39)
    expect(sum(12.12, 13.17)).toBeCloseTo(25.29)
    expect(sum(99.99, 11.11)).toBeCloseTo(111.1)
  })
})

describe("функция subtraction", () => {
  test("Вычетание простых чисел", () => {
    expect(subtraction(1, 2)).toBe(-1)
    expect(subtraction(10, 12)).toBe(-2)
    expect(subtraction(99, 1)).toBe(98)
    expect(subtraction(1, 1)).toBe(0)
    expect(subtraction(1, 0)).toBe(1)
    expect(subtraction(0, 0)).toBe(0)
    expect(subtraction(2, 5)).toBe(-3)
    expect(subtraction(4, 1)).toBe(3)
  })
  test("Вычетание float значений", () => {
    expect(subtraction(0.1, 0.2)).toBeCloseTo(-0.1)
    expect(subtraction(0.1, 2.2)).toBeCloseTo(-2.1)
    expect(subtraction(0.1, 0.29)).toBeCloseTo(-0.19)
    expect(subtraction(12.12, 13.17)).toBeCloseTo(-1.05)
    expect(subtraction(99.99, 11.11)).toBeCloseTo(88.88)
  })
})
describe("функция multiply", () => {
  test("Умножение простых чисел", () => {
    expect(multiply(1, 2)).toBe(2)
    expect(multiply(10, 12)).toBe(120)
    expect(multiply(99, 1)).toBe(99)
    expect(multiply(1, 1)).toBe(1)
    expect(multiply(1, 0)).toBe(0)
    expect(multiply(0, 0)).toBe(0)
    expect(multiply(2, 5)).toBe(10)
    expect(multiply(4, 1)).toBe(4)
  })
  test("Умножение float значений", () => {
    expect(multiply(0.1, 0.2)).toBeCloseTo(0.02)
    expect(multiply(0.1, 2.2)).toBeCloseTo(0.22)
    expect(multiply(0.1, 0.29)).toBeCloseTo(0.029)
    expect(multiply(12.12, 13.17)).toBeCloseTo(159.6204)
    expect(multiply(99.99, 11.11)).toBeCloseTo(1110.8889)
  })
})
describe("функция divide", () => {
  test("Деление простых чисел", () => {
    expect(divide(1, 2)).toBe(0.5)
    expect(divide(10, 12)).toBeCloseTo(0.83)
    expect(divide(99, 1)).toBe(99)
    expect(divide(1, 1)).toBe(1)
    expect(divide(1, 0)).toBe("Ошибка")
    expect(divide(0, 0)).toBe("Ошибка")
    expect(divide(2, 5)).toBe(0.4)
    expect(divide(4, 1)).toBe(4)
  })
  test("Деление float значений", () => {
    expect(divide(0.1, 0.2)).toBeCloseTo(0.5)
    expect(divide(0.1, 2.2)).toBeCloseTo(0.045)
    expect(divide(0.1, 0.29)).toBeCloseTo(0.34482)
    expect(divide(12.12, 13.17)).toBeCloseTo(0.92027)
    expect(divide(99.99, 11.11)).toBeCloseTo(9)
  })
})
